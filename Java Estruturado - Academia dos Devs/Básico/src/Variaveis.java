public class Variaveis {
    public static void main(String[] args) throws Exception {
        // Tipo nome
        byte numeroPequeno = 125;
        short numeroShort = 32767;
        int numeroMaior = 214748364;
        long numeroEnorme = 922337203685477000l;

        //Variaveis do tipo Float
        float peso = 78.7f;
        double peso2 = 78.7; // Não precisa do f

        // variaveis do tipo caractere
        char letra = 'a';
        
        //booleano
        boolean verdadeiro = true;

        // Variável do tipo string
        String nomeCompleto = "Lucas";
        String texto = "Testando com texto maiores";

        System.out.println(numeroPequeno);
        System.out.println(numeroShort);
        System.out.println(numeroMaior);
        System.out.println(numeroEnorme);
        System.out.println(peso);
        System.out.println(peso2);
        System.out.println(letra);
        System.out.println(verdadeiro);
        System.out.println(nomeCompleto);
        System.out.println(texto);
    }
}
